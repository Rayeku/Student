#include "libft.h"

char	*ft_strjoin_gnl(char *s1, char *s2)
{
	size_t	joinlen;
	char	*s3;
	int		i;
	int		j;

	if (!s1 || !s2)
		return (NULL);
	joinlen = ft_strlen(s1) + ft_strlen(s2);
	s3 = malloc(sizeof(char) * (joinlen) + 1);
	if (!s3)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		s3[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j])
		s3[i++] = s2[j++];
	s3[i] = '\0';
	free(s1);
	return (s3);
}

char	*ft_strchr_gnl(char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
			return ((char *)s + i);
		i++;
	}
	if (s[i] == (char)c)
		return ((char *)s + i);
	return (0);
}

int	ft_strlen_gnl(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}
